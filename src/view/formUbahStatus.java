private void ubahStats(){

    String sql = "UPDATE tb_dokumen SET id_status="+selectedStats+" WHERE dokumenId = '"+permohonanId+"';";
    try{    
        
     
    java.sql.PreparedStatement ptsm = conn.prepareStatement(sql);
    ptsm.executeUpdate();
    JOptionPane.showMessageDialog(null, "Data Berhasil di ubah!");
    
    
    
}
catch(Exception e){
System.out.println("setViewText eror");
}
}

