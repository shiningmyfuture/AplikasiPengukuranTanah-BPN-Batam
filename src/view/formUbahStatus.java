    private void btnKirimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnKirimMouseClicked
        // TODO add your handling code here:
        
        if(rb1.isSelected()){
            selectedStats = 5;
            ubahStats();
            this.dispose();
        }
        else if(rb2.isSelected()){
            selectedStats = 6;
            ubahStats();
            this.dispose();
        }
        else if(rb3.isSelected()){
            selectedStats = 7;
            ubahStats();
            changeJobStatsPetugas();
            this.dispose();
        }
        else if(rb4.isSelected()){
            selectedStats = 9;
            ubahStats();
            changeJobStatsPetugas();
            this.dispose();
        }else{
        JOptionPane.showMessageDialog(null, "Anda belum memilih!");
        }
        
        
      
        
        
        
        
        
    }//GEN-LAST:event_btnKirimMouseClicked

