    private void setViewText() {
String query = "SELECT * FROM `db_bapena`.`tb_users` INNER JOIN `db_bapena`.`tb_petugas` ON (`tb_users`.`id_petugas` = `tb_petugas`.`idPetugas`) INNER JOIN `db_bapena`.`tb_divisi` ON (`tb_petugas`.`idDivisi` = `tb_divisi`.`idDivisi`) WHERE id_petugas = '"+idPetugas+"';"   ;
        try{
            
         Statement st = conn.createStatement();
         ResultSet rs = st.executeQuery(query);
         if(rs.next()){
            txtNama.setText(rs.getString("name"));
            txtNik.setText(rs.getString("nik"));
            txtNohp.setText(rs.getString("no_telp"));
            txtAlamat.setText(rs.getString("alamat"));
            txtPassword.setText(rs.getString("password"));
            txtUsername.setText(rs.getString("username"));
            
            divisi.setSelectedItem(rs.getString("nama_divisi"));
            division = rs.getInt("idDivisi");
            
            jobStats.setSelectedIndex(rs.getInt("jobStatus"));
            jobStat = rs.getInt("jobStatus");
            
         }
         }
         
        catch(Exception e){
            System.out.println("setViewText eror");
        }
    }
    private void suntingPetugas() {
          try{
              String queryInsert = "UPDATE tb_petugas SET nik=?, nama_petugas=?, no_telp=? ,alamat=?, idDivisi=?, jobStatus=? WHERE idPetugas = '"+idPetugas+"';";
               String queryInsertUser = "UPDATE tb_users SET username=?, name=?, password=? WHERE id_petugas = '"+idPetugas+"';";
               java.sql.PreparedStatement pstm = conn.prepareStatement(queryInsert);
               java.sql.PreparedStatement pstm2 = conn.prepareStatement(queryInsertUser);
               
               pstm.setString(1, txtNik.getText());
               pstm.setString(2, txtNama.getText());
               pstm.setString(3, txtNohp.getText());
               pstm.setString(4, txtAlamat.getText());
               pstm.setInt(5, division);
               pstm.setInt(6, jobStat);
               pstm.executeUpdate();
               
               pstm2.setString(1, txtUsername.getText());
               pstm2.setString(2, txtNama.getText());
               pstm2.setString(3, txtPassword.getText());
               pstm2.executeUpdate();
               
               JOptionPane.showMessageDialog(null, "Data telah berhasil diubah!");
                       
           
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(rootPane, e);
        }
          }
    
    
    
