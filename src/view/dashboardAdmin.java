private  void hapusUserPemohon(int selectedId){
try{
               String queryInsert = "DELETE FROM `tb_users` WHERE id_users = ? ;";
               
               java.sql.PreparedStatement pstm = conn.prepareStatement(queryInsert);
               
               pstm.setInt(1,selectedId);
              
               pstm.executeUpdate();
               JOptionPane.showMessageDialog(this, "Data berhasil dihapus","succes", JOptionPane.WARNING_MESSAGE);
}
catch(Exception e){
    System.out.println("gagal hapus");
}

}

